#include <libft.h>

char	*ft_strdup(const char *s1)
{
	char	*s;
	size_t	i;

	i = ft_strlen(s1);
	if (!(s = (char *)malloc(sizeof(char) * i + 1)))
		return (0);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
