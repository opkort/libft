#include <libft.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	k;

	k = 0;
	i = 0;
	while (s1[i])
		i++;
	while (s2[k] && k < n)
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (&s1[0]);
}
