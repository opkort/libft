#include <libft.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (n == 0)
		return (1);
	if (!(s1 && s2))
		return (0);
	i = 0;
	while (*s1 == *s2 && *s1 != 0 && *s2 != 0 && i < n - 1)
	{
		s1++;
		s2++;
		i++;
	}
	if (*s1 == *s2)
		return (1);
	else
		return (0);
}
